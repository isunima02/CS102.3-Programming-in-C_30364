#include <stdio.h>
#include <stdlib.h>

int main()
{
    int a;
    float b;
    double ee;
    char vv;
    printf("Enter a integer value-");
    scanf("%d",&a);
    printf("Enter a float value-");
    scanf("%f",&b);
    printf("Enter a double value-");
    scanf("%1f",&ee);
    printf("Enter a char value-");
    scanf("%s",&vv);
    printf("%d %f %f %s",a,b,ee,vv);

    return 0;
}
